// KSA Province Health Data - Real data from GASTAT, MOH, IHME
export interface ProvinceData {
  id: string;
  name: string;
  nameAr: string;
  capital: string;
  population: number;
  popPercent: number;
  coordinates: [number, number]; // [lng, lat] for map markers
  bounds: [[number, number], [number, number]]; // SW, NE corners
  // Disease Prevalence (%)
  diabetes: number;
  hypertension: number;
  cvd: number;
  obesity: number;
  cancer: number; // per 100,000
  // Healthcare Infrastructure
  bedsPerCapita: number; // per 10,000
  phcPerCapita: number; // Primary healthcare centers per 10,000
  physicians: number; // per 10,000
  hospitals: number;
  // Lifestyle Factors
  inactivity: number;
  smoking: number;
  // Mortality (per 100,000)
  diabetesMortality: number;
  cvdMortality: number;
  // Risk Assessment
  undiagnosedRisk: 'low' | 'moderate' | 'high' | 'very high';
  tier: 1 | 2 | 3 | 4; // 1 = Critical, 4 = Monitor
  trend: 'improving' | 'stable' | 'worsening';
}

