// Zambia Health Dashboard Data
// Based on WHO, World Bank, ZDHS, and IHME Global Burden of Disease data

export interface ProvinceData {
  id: string;
  name: string;
  capital: string;
  population: number;
  popPercent: number;
  coordinates: [number, number]; // [lng, lat]
  bounds: [[number, number], [number, number]]; // [[sw], [ne]]

  // Disease prevalence (%)
  diabetes: number;
  hypertension: number;
  cvd: number;
  obesity: number;
  hiv: number; // HIV prevalence - major health burden in Zambia
  malaria: number; // Malaria incidence per 1000
  cancer: number; // per 100,000

  // Infrastructure
  bedsPerCapita: number; // per 10,000
  phcPerCapita: number; // per 10,000
  physicians: number; // per 10,000
  hospitals: number;

  // Lifestyle factors
  inactivity: number;
  smoking: number;

  // Mortality (per 100,000)
  diabetesMortality: number;
  cvdMortality: number;
  hivMortality: number;
  malariaMortality: number;

  // Risk assessment
  undiagnosedRisk: 'low' | 'moderate' | 'high' | 'very high';
  tier: 1 | 2 | 3 | 4;
  trend: 'improving' | 'stable' | 'worsening';
}

